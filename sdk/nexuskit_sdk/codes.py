# sdk/nexuskit_sdk/codes.py — NexusKit 平台统一业务码规范
"""
业务码编码结构：5 位十进制  H H H S S
  ├── HHH（前 3 位）= 对应的 HTTP 状态码
  └── SS （后 2 位）= 业务子码（00 = 通用，01+ = 具体场景）

推算规则：http_status = biz_code // 100
  示例：40103 → 401，40301 → 403，42901 → 429

─────────────────────────────────────────────────────────
前端/客户端判断逻辑（推荐）：
  code == 20000              → 成功
  code == 20100              → 创建成功（可跳转详情）
  40000 <= code < 40100      → 请求参数错误，展示字段提示
  40100 <= code < 40200      → 认证失败，需重新登录
  40300 <= code < 40400      → 权限不足，展示无权限页
  code == 40303              → 会话失效，跳转登录
  code == 40305              → 账号锁定，展示锁定提示
  40900 <= code < 41000      → 资源冲突，提示已存在
  41300 <= code < 41400      → 请求体/文件过大
  code == 41500              → 文件类型不支持
  42200 <= code < 42300      → 业务状态不允许，展示原因
  40100 <= code < 40500      → 认证/参数类错误，前端处理
  code >= 50000              → 服务端错误，展示友好提示
─────────────────────────────────────────────────────────
"""


class BizCode:
    """平台统一业务码命名空间（所有成员均为 int 常量）。

    用法示例：
        from nexuskit_sdk.codes import BizCode

        raise AuthException(code=BizCode.TOKEN_EXPIRED)
        return success(data=..., code=BizCode.CREATED)
    """

    # ── 2xx 成功 ─────────────────────────────────────────────────
    SUCCESS          = 20000  # 通用成功
    CREATED          = 20100  # 资源创建成功

    # ── 400 请求参数错误 ──────────────────────────────────────────
    BAD_REQUEST      = 40000  # 通用参数错误
    PARAM_MISSING    = 40001  # 必填参数缺失
    PARAM_INVALID    = 40002  # 参数格式或值非法
    PARAM_TOO_LONG   = 40003  # 参数值超出长度限制
    IMPORT_ERROR     = 40004  # 导入数据格式错误（Excel/CSV 等批量导入场景）

    # ── 401 认证失败（需重新登录）────────────────────────────────
    UNAUTHORIZED     = 40100  # 通用未认证
    TOKEN_EXPIRED    = 40101  # Token 已过期
    TOKEN_INVALID    = 40102  # Token 无效（签名错误 / 被篡改）
    TOKEN_REVOKED    = 40103  # Token 已吊销（刷新或登出后旧 Token）
    TOKEN_VERSION    = 40104  # Token 版本失效（密码已修改，需重新登录）
    TOKEN_MISSING    = 40105  # 请求未携带 Token
    TOKEN_MISUSE     = 40106  # Token 用途非法（如以 Refresh Token 当 Access Token）
    USER_NOT_FOUND   = 40107  # 用户不存在
    INVALID_CREDS    = 40108  # 账号或密码错误
    SIGN_INVALID     = 40109  # 接口签名验证失败（开放平台 / API Key 场景）
    MFA_REQUIRED     = 40110  # 需要 MFA 二次验证（未完成两步认证）
    MFA_INVALID      = 40111  # MFA 验证码错误或已过期

    # ── 403 权限不足（已认证但无权操作）─────────────────────────
    FORBIDDEN        = 40300  # 通用权限拒绝
    APP_FORBIDDEN    = 40301  # 无该系统访问权限（多系统隔离）
    USER_BANNED      = 40302  # 账号已被封禁（管理员手动禁用）
    SESSION_EXPIRED  = 40303  # 会话已失效（无有效 Refresh Token，需重新登录）
    DATA_SCOPE       = 40304  # 数据权限不足（有接口权限但无权访问该条数据）
    ACCOUNT_LOCKED   = 40305  # 账号已锁定（多次密码错误触发自动锁定）
    ACCOUNT_EXPIRED  = 40306  # 账号已过期（时效性账号超出有效期）

    # ── 404 资源不存在 ────────────────────────────────────────────
    NOT_FOUND        = 40400  # 通用资源未找到

    # ── 405 方法不允许 ────────────────────────────────────────────
    METHOD_NOT_ALLOWED = 40500  # HTTP 方法不支持（如对只读接口发 POST）

    # ── 409 资源冲突 ──────────────────────────────────────────────
    CONFLICT         = 40900  # 通用冲突
    USER_EXISTS      = 40901  # 用户（邮箱 / 用户名）已存在
    ROLE_EXISTS      = 40902  # 角色编码已存在
    DEPT_EXISTS      = 40903  # 部门已存在
    MENU_EXISTS      = 40904  # 菜单路径或编码已存在

    # ── 410 资源已删除（永久不可用）──────────────────────────────
    GONE             = 41000  # 资源已被永久删除（软删除后访问）

    # ── 413 请求体 / 文件过大 ─────────────────────────────────────
    PAYLOAD_TOO_LARGE = 41300  # 请求体超出服务端大小限制
    FILE_TOO_LARGE    = 41301  # 上传文件超出单文件大小限制

    # ── 415 媒体类型不支持 ────────────────────────────────────────
    UNSUPPORTED_MEDIA_TYPE = 41500  # 不支持的文件或内容类型

    # ── 422 Pydantic / FastAPI 参数校验失败 ──────────────────────
    UNPROCESSABLE    = 42200  # 请求体结构或字段类型不匹配
    BUSI_STATUS      = 42201  # 当前业务状态不允许此操作（如已发布不可删除）
    RELATION_EXISTS  = 42202  # 存在关联数据，无法删除 / 修改

    # ── 429 限流 ──────────────────────────────────────────────────
    TOO_MANY         = 42900  # 通用请求频次超限
    REFRESH_CONFLICT = 42901  # 并发刷新 Token 冲突（防重放）
    LOGIN_TOO_MANY   = 42902  # 登录尝试次数超限（触发锁定前的提示）

    # ── 500 服务器内部错误 ────────────────────────────────────────
    INTERNAL_ERROR   = 50000  # 通用服务器内部错误
    DB_ERROR         = 50001  # 数据库操作失败
    CACHE_ERROR      = 50002  # 缓存服务（Redis）异常
    IO_ERROR         = 50003  # 文件 / IO 操作失败

    # ── 501 功能未实现 ────────────────────────────────────────────
    NOT_IMPLEMENTED  = 50100  # 功能暂未实现（占位接口）

    # ── 502 网关上游异常 ──────────────────────────────────────────
    BAD_GATEWAY      = 50200  # 上游服务返回非预期响应

    # ── 503 上游服务不可用 ────────────────────────────────────────
    UNAVAILABLE      = 50300  # 上游 / 依赖服务不可用

    # ── 504 网关超时 ──────────────────────────────────────────────
    GATEWAY_TIMEOUT  = 50400  # 上游服务响应超时

    # ─────────────────────────────────────────────────────────────

    @staticmethod
    def http_status(biz_code: int) -> int:
        """根据业务码推算对应的 HTTP 状态码（取前 3 位）。

        示例：
            BizCode.http_status(40103)  # → 401
            BizCode.http_status(40301)  # → 403
            BizCode.http_status(42901)  # → 429
        """
        return biz_code // 100
